    private void LoadData(List<HoaDonModel> ListHDS) {
        dtm.setRowCount(0);
        for (HoaDonModel HD : ListHDS) {
            dtm.addRow(HD.toDataRow());
        }
    }

    private void showData(HoaDonModel Hd) {
        txtTrangThai.setText(String.valueOf(Hd.getTrangThai()));
        txtNgayTao.setText(Hd.getNgayTao());
        txtmaNV.setText(String.valueOf(Hd.getMaNV()));
        txtMaKH.setText(String.valueOf(Hd.getMaKH()));
        txtMaKM.setText(String.valueOf(Hd.getMaKM()));
    }

    public void ResetForm() {
        txtTrangThai.setText("");
        txtNgayTao.setText("");
        txtmaNV.setText("");
        txtMaKH.setText("");
        txtMaKM.setText("");
    }

